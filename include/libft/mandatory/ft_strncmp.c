#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] && s1[i] == s2[i] && i < n - 1)
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}

/*
	Compara as strings enquanto elas forem iguais e estiverem dentro do limite
de 'n', no momento em que elas diferirem retorna um valor numerico q diz se sao
iguais, se 's1' Ã© maior ou menor de 's2'.
*/